//Program to show use of variables

#include<iostream>
using namespace std;

int main(){
    int age = 20;         //integer type variable
    char gender = 'M';    //char type variable (notice the use of '' for character)
    float perc = 73.65;   //float for decimal values
    /* 
        endl is used to shift to next line or new line in console. 
        "\n" also does the same.
    */

    cout<<"My age is: "<<age<<endl;
    cout<<"My gender: "<<gender<<"\n";
    cout<<perc<<"% is my average percentage\n";
    
    return 0;
}

//end of program




/* OUTPUT

My age is: 20
My gender: M
73.65% is my average percentage

*/